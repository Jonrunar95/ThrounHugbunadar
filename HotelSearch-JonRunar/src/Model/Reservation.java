package Model;

import java.util.Date;

/**
 *
 * @author Notandi
 */
public class Reservation {
    private Room room;
    private User user;
    private String date;
    
    public Room getRoom() {
        return room;
    }

    public void setRoom(Room room) {
        this.room = room;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
